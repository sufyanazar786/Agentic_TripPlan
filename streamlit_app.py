st.subheader("üí¨ How can I help you in planning a trip?")
st.write("Tell me your destination, duration, or preferences and I‚Äôll generate a plan for you.")

# --- Input Form ---
with st.form(key="query_form", clear_on_submit=True):
    user_input = st.text_input(
        "Enter your request",
        placeholder="e.g. Plan a trip to Goa for 5 days"
    )
    submit_button = st.form_submit_button("Generate Plan")

# --- Handle Submission ---
if submit_button and user_input.strip():
    try:
        with st.spinner(" Agentic Bot is thinking ..."):
            payload = {"question": user_input}
            response = requests.post(f"{BASE_URL}/query", json=payload)

        if response.status_code == 200:
            answer = response.json().get("answer", "No answer returned.")
            markdown_content = f"""
            ## AI Travel Plan

            **Generated:** {datetime.datetime.now().strftime('%Y-%m-%d at %H:%M')}  
            **Created by:** Sufyan's Travel Agent  

            ---

            {answer}

            ---

            ‚ö†Ô∏è *This travel plan was generated by AI. Please verify all information, especially prices, operating hours, and travel requirements before your trip.*
            """
            st.markdown(markdown_content, unsafe_allow_html=True)
        else:
            st.error("Bot failed to respond: " + response.text)

    except Exception as e:
        st.error(f"The response failed due to: {e}")

