import streamlit as st
import requests
import datetime

BASE_URL = "https://agentic-tripplan.onrender.com"  # Backend endpoint

st.set_page_config(
    page_title="üåç Travel Planner Agentic Application",
    page_icon="üåç",
    layout="centered",
    initial_sidebar_state="expanded",
)

# ---------- Dark theme CSS ----------
